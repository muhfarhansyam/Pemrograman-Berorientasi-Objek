    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("No.Jersey");
        model.addColumn("Nama Jersey");
        model.addColumn("Merk");
        model.addColumn("Ukuran");
        model.addColumn("Jumlah");
        model.addColumn("Harga Satuan");
        
        try{
            int no =1;
            String sql = "SELECT * From jerseyshop";
            java.sql.Connection conn = (Connection)KoneksiSql.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet rslt = stm.executeQuery(sql);
            
            while(rslt.next()){
                model.addRow(new Object[]{no++,rslt.getString(1),rslt.getString(2),rslt.getString(3),rslt.getString(4),rslt.getString(5),rslt.getString(6)});            
            }
            jerseyshop.setModel(model);
            
        } catch (SQLException e){
            System.out.println("Error : " +e.getMessage());
        }
        }
    
