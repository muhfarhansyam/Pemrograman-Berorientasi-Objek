    private void tbsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbsimpanActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO jerseyshop VALUES('"+txtnojersey.getText()+"','"+txtnamajersey.getText()+"','"+cbmerk.getSelectedItem()+"','"+txtukuran.getText()+"','"+txtjumlah.getText()+"','"+txtHsatuan.getText()+"')";
            java.sql.Connection conn = (Connection)KoneksiSql.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null,"Proses Penyimpanan Data Barang Berhasil");
            tampilkan_data();
            clear_form();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            System.out.println("Penyimpanan Data Gagal");
        }
    }//GEN-LAST:event_tbsimpanActionPerformed

