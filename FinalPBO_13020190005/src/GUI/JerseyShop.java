    private void jerseyshopMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jerseyshopMouseClicked
        // TODO add your handling code here:
        int baris = jerseyshop.rowAtPoint(evt.getPoint());
        String no_jersey = jerseyshop.getValueAt(baris,1).toString();
        txtnojersey.setText(no_jersey);
        String nama_jersey = jerseyshop.getValueAt(baris,2).toString();
        txtnamajersey.setText(nama_jersey);
        String merk = jerseyshop.getValueAt(baris,3).toString();
        cbmerk.setSelectedItem(merk);
        String ukuran = jerseyshop.getValueAt(baris,4).toString();
        txtukuran.setText(ukuran);
        String jumlah = jerseyshop.getValueAt(baris,5).toString();
        txtjumlah.setText(jumlah);
        String harga_satuan = jerseyshop.getValueAt(baris,6).toString();
        txtHsatuan.setText(harga_satuan);
        
    }//GEN-LAST:event_jerseyshopMouseClicked

